package jrJava.practice6;

public class UseOfForLoop {

	public static void main(String[] args) {
		
		
		int i;
		
		for(i=1; i<=10; i++){
			
			System.out.println("Sir is so handsome!");
			System.out.println("Sir has a wonderful voice!");
			System.out.println("i=" + i);
			System.out.println();
			
		}
		
		System.out.println("I am out of the loop. i=" + i);
	}

}
 